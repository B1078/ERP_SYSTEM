        public IActionResult GET()
        {
            try
            {
             
                string Query = @"select CMPM.*,cm.CntryName as RegCountry,sm.StateName as RegState,
cm1.CntryName as CommCountry , sm1.StateName as CommState
from Cmp_Mst CMPM WITH (NOLOCK)
inner join State_Mst SM on SM.StateId = CMPM.CmpRgstateId 
inner join Country_Mst CM on CM.CntryId = CMPM.cmpRgcntryId 
inner join State_Mst SM1 on SM1.StateId = CMPM.CmpCstId 
inner join Country_Mst CM1 on CM1.CntryId = CMPM.CmpCcntId ";
                List<Dictionary<string, object>> dataList = new List<Dictionary<string, object>>();
                using (SqlConnection con = new SqlConnection(ConnectionString))
                {
                    using (SqlCommand cmd = new SqlCommand(Query, con))
                    {
                        con.Open();
                        cmd.CommandText = Query;
                        cmd.CommandTimeout = 300;
                        SqlDataReader rdr = cmd.ExecuteReader();
                        {
                            while (rdr.Read())
                            {
                                Dictionary<string, object> row = new Dictionary<string, object>();
                                for (int i = 0; i < rdr.FieldCount; i++)
                                {
                                    string columnName = rdr.GetName(i);
                                    object? value = rdr.IsDBNull(i) ? null : rdr.GetValue(columnName);
                                    // Convert DATE values to string format without time
                                    if (value is DateTime dateValue && dateValue.TimeOfDay == TimeSpan.Zero)
                                    {
                                        row[columnName] = dateValue.ToString("dd/MM/yyyy");
                                    }
                                    else
                                    {
                                        row[columnName] = value!;
                                    }
                                }
                                dataList.Add(row);
                            }
                        }
                        con.Close();
                    }
                }
                return Json(dataList);
            }
            catch (Exception ex)
            {
                return StatusCode(500, ex.Message.ToString());
            }
        }
